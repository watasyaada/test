
public class person {
  private String name = null;
  private int age = 0;
  
  public person(String name, int age){
	  this.name = name;
	  this.age = age;
  }
  
  public String getName(){
	  return this.name;
  }
  
  public void setName(String name, int age){
	  this.name = name;
	  this.age = age;
  }
}
